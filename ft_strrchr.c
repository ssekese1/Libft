#include "libft.h"

char	*ft_strrchr(char *s, int c)
{
	int		i;
	char	ch;

	ch = (char)c;
	i = 0;
	while (s[i] != '\0')
		i++;
	while (i >= 0)
	{
		if (s[i] == ch)
			return (&s[i]);
		i--;
	}
	return (NULL);
}
